/**
 * 在 Monaco Editor 加载之前调用此函数以启用中文界面。
 * 必须在任何 `import * as monaco from 'monaco-editor'` 之前执行。
 */
export function setupMonacoLocale(): void {
  // 构建完整的消息数组（未翻译的位置为 undefined，Monaco 会回退到英文）
  const maxIndex = Math.max(...Object.keys(zhCN).map(Number))
  const messages: (string | undefined)[] = new Array(maxIndex + 1).fill(undefined)
  for (const [idx, text] of Object.entries(zhCN)) {
    messages[Number(idx)] = text
  }

  ;(globalThis as any)._VSCODE_NLS_MESSAGES = messages
  ;(globalThis as any)._VSCODE_NLS_LANGUAGE = 'zh-cn'
}
